CREATE DATABASE db_discord;

CREATE TABLE Guild
(
    guildID VARCHAR(100) NOT NULL PRIMARY KEY,
    guildOwnerID VARCHAR(100) NOT NULL
);

CREATE TABLE GuildConfigurable
(
    guildID VARCHAR(100) NOT NULL PRIMARY KEY,
    guildLanguage VARCHAR(10) DEFAULT 'es',
    cmdPrefix VARCHAR(10) DEFAULT 's!'
);
